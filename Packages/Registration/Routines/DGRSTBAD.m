 . S DGLINE=DGLINE+1
 . S MSG(DGLINE)=""
 . S DGLINE=DGLINE+1
 . S MSG(DGLINE)=DGRPTYP(DGPTYP)
 . S DGLINE=DGLINE+1
 . S MSG(DGLINE)=""
 . S DGTXT=""
