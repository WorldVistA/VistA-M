 S DGRPTYP(2)="Foreign Addresses Only"
 S DIR("B")=1
 S DIR(0)="S^1:"_DGRPTYP(1)_";2:"_DGRPTYP(2)_";3:Both"
 D ^DIR G:$D(DIRUT) EXIT
 S DGRPTYP=Y
 S MSG(1)=""
