 S %DT("A")="Requested Start Time: ",%DT="FATE"
 S %DT(0)="NOW",%DT("B")="NOW" D ^%DT K %DT(0) I Y<0 Q
 S ZTDTH=Y
 S ZTDESC="INVALID STATE/INACTIVE COUNTY REPORT"
 S ZTRTN="PROC^"_DGNS_"(DGNS,.DGRPTYP)"
 D ^%ZTLOAD
 I $D(ZTSK) D
 .W !!,"REPORT QUEUED"
 E  W !!,"REPORT NOT QUEUED"
 Q
