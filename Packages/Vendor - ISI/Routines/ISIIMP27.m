 S FDA(9000010.13,"+1,",.02)=DFN
 S FDA(9000010.13,"+1,",.03)=VISIT
 S FDA(9000010.13,"+1,",1201)=DATETIME
 S FDA(9000010.13,"+1,",1202)=PROVIDER
 S FDA(9000010.13,"+1,",1204)=PROVIDER
