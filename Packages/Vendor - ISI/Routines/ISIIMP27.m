 ;
 S COMMENT=$G(ISIMISC("COMMENT")) ;FREE TEXT COMMENTS
 S PACKAGE=35 ;ORDER ENTRY/RESULTS REPORTING
 S SOURCE=36 ;TEXT INTEGRATION UTILITIES
 ;
