 S EXPNM=$G(PATDAT(52.46,EXPIENS,.01,"E"))
 S EXPDOB=$G(PATDAT(52.46,EXPIENS,.08,"E"))
 S EXPSSN=$G(PATDAT(52.46,EXPIENS,1.4,"E"))
