 ;DX - update DX identifier(s)
ICDDX ;Update Dx
 D BMES^XPDUTL(">>> UPDATING DX CODE 730.28 IDENTIFIER...")
 N LINE,X,ICDDXDA,DA,DIE,IDENT,DR
