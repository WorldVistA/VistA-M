FIRST ;
 S B2=$S(B2]"":B2,1:^LAB(62.06,"AI",LRBN,B1))
 S:$E(B2)'="R"&("A"[B3) LRFLAG=1
 S LR1PASS(LRBN)=B1_U_B2_U_B3,^LR(LRDFN,"MI",LRIDT,3,LRBUG,LRBN)=LR1PASS(LRBN)
