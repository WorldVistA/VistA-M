 . N X
 . S X=$S(B3="N"!(B3="R"&LRFLAG):B1_"*",1:B1)
 . S $P(LRRES(LRBN),U,A)=X
 . S X=$S(B3="N"!(B3="R"&LRFLAG):B2_"*",1:B2)
 . S $P(LRINT(LRBN),U,A)=X
 ;
 I B3=""!(B3="A")!(B3="R"&'LRFLAG) S $P(LRRES(LRBN),U,A)=B1,$P(LRINT(LRBN),U,A)=B2
 ;
 Q
