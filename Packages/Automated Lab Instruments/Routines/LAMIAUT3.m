 ; Check if entry is for a bacterial drug, not an AFB drug.
 S J=$P(^LAB(62.06,B,0),U,2)
 I J="" Q
 I $D(LRINT(J)),LRINT(J)'?."^" W !,$E($P(^LAB(62.06,B,0),U),1,14) S LRDCOM=$P(^(0),U,3),LRACNT=LRACNT+1 D SIR
